Program *snocone_parse_program(const char *src, const char *filename);

/* ---- harness ---- */
static int g_pass = 0, g_fail = 0;

#define ASSERT(cond, fmt, ...) do { \
    if (cond) { g_pass++; } \
    else { fprintf(stderr, "FAIL %s:%d: " fmt "\n", __func__, __LINE__, ##__VA_ARGS__); g_fail++; } \
} while(0)

/* Walk the linked-list and collect a STMT_t* array (caller must free). */
static STMT_t **collect_stmts(Program *prog, int *out_n) {
    int n = 0;
    for (STMT_t *s = prog->head; s; s = s->next) n++;
    STMT_t **arr = calloc(n, sizeof *arr);
    int i = 0;
    for (STMT_t *s = prog->head; s; s = s->next) arr[i++] = s;
    *out_n = n;
    return arr;
}

/* True if stmt is a bare label-only landing pad (no subject, no goto). */
static int is_label_pad(STMT_t *s) { return s->label && !s->subject && !s->go; }
/* True if stmt is a goto-only statement (no subject). */
static int is_goto_uncond(STMT_t *s) { return s->go && s->go->uncond && !s->subject; }
/* True if stmt is a bare expression (subject, no goto). */
static int is_bare_expr(STMT_t *s) { return s->subject && !s->go; }
/* True if stmt is an assignment (subject + replacement, has_eq). */
static int is_assignment(STMT_t *s) { return s->subject && s->replacement && s->has_eq; }

/* =========================================================================
 * Test 1 — bare goto
 *   goto top;
 *   Expected:
 *     stmt[0] :(top)             (goto-uncond, no subject)
 * ========================================================================= */
static void test_goto_bare(void) {
    Program *prog = snocone_parse_program("goto top;", "test");
    ASSERT(prog != NULL, "parse failed");
    if (!prog) return;

    int n; STMT_t **stmts = collect_stmts(prog, &n);
    ASSERT(n == 1, "expected 1 stmt, got %d", n);
    if (n >= 1) {
        ASSERT(is_goto_uncond(stmts[0]), "stmt[0] should be goto-uncond");
        ASSERT(stmts[0]->go && stmts[0]->go->uncond &&
               strcmp(stmts[0]->go->uncond, "top") == 0,
               "goto target should be \"top\", got %s",
               stmts[0]->go && stmts[0]->go->uncond ? stmts[0]->go->uncond : "(null)");
        ASSERT(stmts[0]->subject == NULL, "goto stmt should have no subject");
        ASSERT(stmts[0]->label == NULL, "goto stmt should have no label");
    }
    free(stmts);
}

/* =========================================================================
 * Test 2 — goto inside a sequence
 *   x = 1; goto fin; y = 2;
 *   Expected:
 *     stmt[0] x = 1
 *     stmt[1] :(fin)
 *     stmt[2] y = 2
 * ========================================================================= */
static void test_goto_in_sequence(void) {
    Program *prog = snocone_parse_program("x = 1; goto fin; y = 2;", "test");
    ASSERT(prog != NULL, "parse failed");
    if (!prog) return;

    int n; STMT_t **stmts = collect_stmts(prog, &n);
    ASSERT(n == 3, "expected 3 stmts, got %d", n);
    if (n >= 1) ASSERT(is_assignment(stmts[0]), "stmt[0] should be assignment");
    if (n >= 2) {
        ASSERT(is_goto_uncond(stmts[1]), "stmt[1] should be goto");
        ASSERT(stmts[1]->go && stmts[1]->go->uncond &&
               strcmp(stmts[1]->go->uncond, "fin") == 0,
               "goto target should be \"fin\"");
    }
    if (n >= 3) ASSERT(is_assignment(stmts[2]), "stmt[2] should be assignment");
    free(stmts);
}


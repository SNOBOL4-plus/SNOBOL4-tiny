/* =========================================================================
 * Test 11 — label inside a function body
 *   function F() { again: x = 1; goto again; }
 *   Expected:
 *     stmt[0] DEFINE('F()')
 *     stmt[1] :(F_end)
 *     stmt[2] F           (entry-point label)
 *     stmt[3] again       (user label)
 *     stmt[4] x = 1
 *     stmt[5] :(again)
 *     stmt[6] F_end
 * ========================================================================= */
static void test_label_inside_function(void) {
    Program *prog = snocone_parse_program(
        "function F() { again: x = 1; goto again; }", "test");
    ASSERT(prog != NULL, "parse failed");
    if (!prog) return;

    int n; STMT_t **stmts = collect_stmts(prog, &n);
    ASSERT(n == 7, "expected 7 stmts, got %d", n);
    if (n >= 1) ASSERT(is_bare_expr(stmts[0]), "stmt[0] should be DEFINE bare-expr");
    if (n >= 2) ASSERT(is_goto_uncond(stmts[1]), "stmt[1] should be skip-goto");
    if (n >= 3) {
        ASSERT(is_label_pad(stmts[2]), "stmt[2] should be entry label F");
        ASSERT(stmts[2]->label && strcmp(stmts[2]->label, "F") == 0,
               "stmt[2] label should be \"F\"");
    }
    if (n >= 4) {
        ASSERT(is_label_pad(stmts[3]), "stmt[3] should be user label again");
        ASSERT(stmts[3]->label && strcmp(stmts[3]->label, "again") == 0,
               "stmt[3] label should be \"again\"");
    }
    if (n >= 5) ASSERT(is_assignment(stmts[4]), "stmt[4] should be assignment");
    if (n >= 6) {
        ASSERT(is_goto_uncond(stmts[5]), "stmt[5] should be goto");
        ASSERT(stmts[5]->go && stmts[5]->go->uncond &&
               strcmp(stmts[5]->go->uncond, "again") == 0,
               "stmt[5] target should be \"again\"");
    }
    if (n >= 7) {
        ASSERT(is_label_pad(stmts[6]), "stmt[6] should be F_end pad");
        ASSERT(stmts[6]->label && strcmp(stmts[6]->label, "F_end") == 0,
               "stmt[6] label should be \"F_end\"");
    }
    free(stmts);
}

